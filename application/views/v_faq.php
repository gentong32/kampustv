                  Apakah TV Kampus sama dengan upload YouTube?<br>
                  <span style="font-weight: normal">
                      Kita memanfaatkan YouTube sebagai Media Aset Management online untuk bahan siaran.
					  Jadi semua konten harus ada di YouTube (diunggah ke YouTube dulu) baru linknya diunggah ke aplikasi.
					  Di aplikasi konten tersebut harus disetujui oleh verifikator prodi untuk bisa tampil di Ensiklomedia.
                  </span>
              </li>
              <li style="font-weight: bold">
                  Apakah kelebihan TV Kampus dibanding dengan upload YouTube biasa?<br>
                  <span style="font-weight: normal">
                      Kelebihannya adalah konten telah diverifikasi oleh pengelola/verifikator prodi.
					  Khusus untuk konten pembelajaran dosen harus menyesuaikan silabus yang sudah ada di database.
                  </span>
              </li>
              <li style="font-weight: bold">
                  Dalam beberapa contoh jadwal program ada jam-jamnya. Apakah pembelajaran sekolah yang tidak pakai jam bisa diputar orang? <br>
                  <span style="font-weight: normal">
                      Ada 4 fitur utama di TV Kampus:
                      <ol>
                          <li>Channel TV Prodi (TV Streaming) yang mengalir berdasarkan jadwal siaran.</li>
                          <li>Ensiklomedia (video on demand) yang bisa diputar kapan saja sesuai keinginan.</li>
                      <li>Kelas Virtual dimana dosen dapat membuat kelas virtual dengan menyusun beberapa video pembelajaran dalam sebuah learning path(playlist mini) yang dilengkapi dengan soal latihan dan fitur lainnya.</li>
						  <li>Festival TV Kampus yaitu kegiatan di seluruh fitur TV Kampus dilombakan dalam event tahunan mulai tingkat kabupaten kota, propinsi, dan nasional <i>(underconstruction)</i>.</li>
